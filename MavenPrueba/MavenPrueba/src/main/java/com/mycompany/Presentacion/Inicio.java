    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try {
            String cedulaText = jTextField1.getText().trim();
            if (cedulaText.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Por favor, ingrese una cédula para eliminar.");
                return;
            }
            int cedula = Integer.parseInt(cedulaText);
            Cliente cliente = clienteController.findCliente(cedula);
            if (cliente == null) {
                JOptionPane.showMessageDialog(this, "No se encontró un cliente con esa cédula.");
                return;
            }
            int confirm = JOptionPane.showConfirmDialog(this, "¿Está seguro de eliminar este cliente?", "Confirmar eliminación", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                clienteController.destroy(cedula);
                clearFields();
                JOptionPane.showMessageDialog(this, "Cliente eliminado correctamente.");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Por favor, ingrese una cédula válida.");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al eliminar datos: " + e.getMessage());
        }
    }                                        

