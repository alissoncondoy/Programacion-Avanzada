    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String cedulaText = jTextField1.getText().trim();
            if (cedulaText.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Por favor, ingrese una cédula para buscar.");
                return;
            }
            int cedula = Integer.parseInt(cedulaText);
            Cliente cliente = clienteController.findCliente(cedula);
            if (cliente == null) {
                JOptionPane.showMessageDialog(this, "No se encontró un cliente con esa cédula.");
                return;
            }
            jTextField2.setText(cliente.getNombre());
            jTextField3.setText(cliente.getApellido());
            jTextField4.setText(cliente.getDireccion());
            jTextField5.setText(cliente.getTelefono());
            jTextField6.setText(String.valueOf(cliente.getSaldo()));
            JOptionPane.showMessageDialog(this, "Datos encontrados.");
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Por favor, ingrese una cédula válida.");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al buscar datos: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            // Validar que todos los campos estén llenos
            String cedulaText = jTextField1.getText().trim();
            String nombre = jTextField2.getText().trim();
            String apellidos = jTextField3.getText().trim();
            String direccion = jTextField4.getText().trim();
            String telefono = jTextField5.getText().trim();
            String saldoText = jTextField6.getText().trim();

            if (cedulaText.isEmpty() || nombre.isEmpty() || apellidos.isEmpty() ||
                direccion.isEmpty() || telefono.isEmpty() || saldoText.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios.");
                return;
            }

            // Convertir los campos numéricos
            int cedula = Integer.parseInt(cedulaText);
            int saldo = Integer.parseInt(saldoText);

            // Crear un nuevo objeto Cliente y asignar los valores
            Cliente cliente = new Cliente();
            cliente.setCedula(cedula);
            cliente.setNombre(nombre);
            cliente.setApellido(apellidos);
            cliente.setDireccion(direccion);
            cliente.setTelefono(telefono);
            cliente.setSaldo(saldo);

            // Guardar en la base de datos
            clienteController.create(cliente);
            JOptionPane.showMessageDialog(this, "Cliente guardado correctamente.");
            clearFields();
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Por favor, ingrese valores numéricos válidos para cédula y saldo.");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al guardar datos: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

